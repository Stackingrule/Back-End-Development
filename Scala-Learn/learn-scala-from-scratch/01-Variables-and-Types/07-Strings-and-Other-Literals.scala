// Integer Literals #
val hex: Int = 0x0F5
val dec: Int = 245

val hex1: Long = 0x0A3DE5L //The L at the end is for Long
val dec1: Long = 671205L

// Driver Code
println(hex)
println(dec)
println(hex1)
println(dec1)

// Floating-Point Literals
val floatLiteral: Float = 1.2345F // The F at the end is for Float
